// Function to build query string with parameters
function getQueryString($exclude = []) {
    global $searchKeyword, $categoryFilter;
    $params = [];
    if (!empty($searchKeyword) && !in_array('search', $exclude)) $params[] = 'search=' . urlencode($searchKeyword);
    if (!empty($categoryFilter) && !in_array('category', $exclude)) $params[] = 'category=' . urlencode($categoryFilter);
    return !empty($params) ? '?' . implode('&', $params) : '';
}
