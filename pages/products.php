// Export table to CSV
function exportTableToCSV(filename) {
    let csv = [];
    const rows = document.querySelectorAll("table tr");

    for (let row of rows) {
        const cols = row.querySelectorAll("td, th");
        const rowData = Array.from(cols).map(col => `"${col.innerText.trim().replace(/"/g, '""')}"`);
        csv.push(rowData.join(","));
    }

    const csvFile = new Blob([csv.join("\n")], { type: "text/csv" });
    const downloadLink = document.createElement("a");
    downloadLink.href = URL.createObjectURL(csvFile);
    downloadLink.download = filename;
    document.body.appendChild(downloadLink);
    downloadLink.click();
    document.body.removeChild(downloadLink);
}

// Show success message if exists in URL
function showSuccessMessage() {
    const urlParams = new URLSearchParams(window.location.search);
    if (urlParams.has('success')) {
        const toastEl = document.getElementById('successToast');
        const toast = new bootstrap.Toast(toastEl);
        toast.show();
        
        // Remove success parameter from URL without page refresh
        const newUrl = window.location.pathname;
        window.history.replaceState({}, document.title, newUrl);
        
        // Auto-hide after 5 seconds
        setTimeout(() => {
            toast.hide();
        }, 5000);
    }
}

// Call the function when page loads
document.addEventListener('DOMContentLoaded', showSuccessMessage);
